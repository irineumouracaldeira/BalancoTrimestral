class BalancoTrimestral {
	public static void main (String[] args) {
	// Declare variável inteira gastosJaneiro inicialize-a com 15000

	int gastosJaneiro = 15000;

	// Declare variável inteira gastosFevereiro inicialize-a com 23000

	int gastosFevereiro = 23000;

	// Declare variável inteira gastosMarco inicialize-a com 17000

	int gastosMarco = 17000;

	// Crie uma variável chamada gastosTrimestre e inicialize-a com a soma das outras 3 variáveis

	int gastosTrimestre = gastosJaneiro + gastosFevereiro + gastosMarco;

	// imprima variável gastosTrimestre

	System.out.println(gastosTrimestre);

	}
}
 